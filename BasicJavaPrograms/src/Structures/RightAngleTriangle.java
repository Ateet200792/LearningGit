package Structures;

public class RightAngleTriangle {

	public static void main(String[] args) {
	
		
for (int i= 1;i<=5;i++)
{
	for(int j=1;j<=i;j++)
	{

	
	
		System.out.print("*"); // We can print i or j also in place of * to get the pattern in numbers.
		
	}
		System.out.println();
	
}
	}

}
